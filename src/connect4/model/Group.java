package connect4.model;

/** Models the groups of a {@link Game}. */
public enum Group {

    /**
     * Individual representation for vertical groups.
     */
    VERTICAL,

    /**
     * Individual representation for horizontal groups.
     */
    HORIZONTAL,

    /**
     * Individual representation for ascending groups.
     */
    ASCENDING,

    /**
     * Individual representation for descending groups.
     */
    DESCENDING
}
